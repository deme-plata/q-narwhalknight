/// Validate and apply a game action from the tick pipeline.
///
/// This is used internally by `tick::tick` for processing queued player actions
/// and AI-generated NPC actions.
pub(crate) fn process_action_internal(
    world: &mut GameWorld,
    wallet: &str,
    action: GameAction,
) -> Result<(), String> {
    if wallet.is_empty() {
        return process_npc_action(world, action);
    }

    let qa = QueuedAction {
        wallet: wallet.to_string(),
        action,
        submitted_turn: world.meta.turn,
    };
    let _ = process_action(world, &qa);
    Ok(())
}

/// Process an NPC action (no wallet).
fn process_npc_action(world: &mut GameWorld, action: GameAction) -> Result<(), String> {
    let turn = world.meta.turn;
    match action {
        GameAction::RaiseArmy { province } => {
            let prov = world.province(province).ok_or("Province not found")?;
            let controller = prov.controller;
            let population = prov.population;

            let levy = (population / 20).max(50);
            let maa = (population / 100).max(10);
            let knights = (population / 1000).max(1) as u16;

            let army_id = world.alloc_army_id();
            world.dirty.dirty_armies.insert(army_id);
            world.dirty.armies_added.push(army_id);
            world.armies.push(Army {
                id: army_id,
                owner_faction: controller,
                commander: None,
                troops: Troops { levy, men_at_arms: maa, knights },
                morale: FixedPoint::from_int(600),
                location: province,
                destination: None,
                movement_queue: Vec::new(),
                raised_turn: turn,
                supply: FixedPoint::from_int(100),
                siege: None,
            });
            Ok(())
        }
        GameAction::MoveArmy { army, target } => {
            let adjacent = world.army(army)
                .and_then(|a| world.province(a.location))
                .map(|p| p.neighbors.contains(&target))
                .unwrap_or(false);
            if !adjacent {
                return Err("Target not adjacent".to_string());
            }
            if let Some(a) = world.army_mut_dirty(army) {
                a.destination = Some(target);
                a.movement_queue.clear();
            }
            Ok(())
        }
        GameAction::MoveArmyPath { army, target } => {
            let location = world.army(army).map(|a| a.location);
            if let Some(loc) = location {
                if let Some(path) = map::find_path(loc, target) {
                    if !path.is_empty() {
                        if let Some(a) = world.army_mut_dirty(army) {
                            a.movement_queue = path;
                            a.destination = None;
                        }
                    }
                }
            }
            Ok(())
        }
        GameAction::DeclareWar { target, casus_belli } => {
            let target_id = target;
            // Find which NPC faction borders the target.
            let attacker = {
                let mut found = None;
                for realm in &world.realms {
                    if realm.owner_wallet.is_empty() && !realm.at_war_with.contains(&target_id) {
                        let borders = world.provinces.iter().any(|p| {
                            p.controller == realm.faction
                                && p.neighbors.iter().any(|&n| {
                                    world.province(n).map_or(false, |np| np.controller == target_id)
                                })
                        });
                        if borders {
                            found = Some(realm.faction);
                            break;
                        }
                    }
                }
                found.ok_or("No NPC faction found to declare war")?
            };

            let realm_count = world.realms.len();
            for ridx in 0..realm_count {
                if world.realms[ridx].faction == attacker && !world.realms[ridx].at_war_with.contains(&target_id) {
                    world.realms[ridx].at_war_with.push(target_id);
                    world.dirty.dirty_realms.insert(attacker);
                }
                if world.realms[ridx].faction == target_id && !world.realms[ridx].at_war_with.contains(&attacker) {
                    world.realms[ridx].at_war_with.push(attacker);
                    world.dirty.dirty_realms.insert(target_id);
                }
            }
            if let Some(rel) = world.relation_mut_dirty(attacker, target_id) {
                rel.at_war = true;
                rel.opinion -= FixedPoint::from_int(100);
                rel.grievances.push(Grievance {
                    reason: format!("Declared war ({:?})", casus_belli),
                    opinion_modifier: FixedPoint::from_int(-100),
                    inflicted_turn: turn,
                    decay_turns_remaining: 200,
                });
            }
            Ok(())
        }
        GameAction::ProposeTreaty { target, treaty } => {
            if treaty == TreatyType::WhitePeace {
                let proposer = world.realms.iter()
                    .find(|r| r.owner_wallet.is_empty() && r.at_war_with.contains(&target))
                    .map(|r| r.faction);
                if let Some(p) = proposer {
                    return apply_treaty(world, p, target, treaty);
                }
            }
            Err("NPC treaty proposal not handled".to_string())
        }
        GameAction::BuildImprovement { province, improvement } => {
            let prov = world.province(province).ok_or("Province not found")?;
            let controller = prov.controller;
            let cost = improvement.build_cost_gold();
            let can_afford = world.realm_for_faction(controller)
                .map(|r| r.treasury >= cost)
                .unwrap_or(false);
            let already_built = prov.improvements.contains(&improvement);
            let in_queue = prov.construction_queue.iter().any(|&(imp, _)| imp == improvement);

            if can_afford && !already_built && !in_queue {
                if let Some(realm) = world.realm_for_faction_mut_dirty(controller) {
                    realm.treasury = realm.treasury - cost;
                }
                if let Some(p) = world.province_mut_dirty(province) {
                    p.construction_queue.push((improvement, improvement.build_turns()));
                }
            }
            Ok(())
        }
        _ => Ok(()),
    }
}

