// ---------------------------------------------------------------------------
// Public API: tick (10-step turn pipeline)
// ---------------------------------------------------------------------------

/// Advance the world by one game turn.  Returns a `TurnSummary` of everything
/// that happened.  `block_hash` provides the per-turn entropy.
///
/// The 10-step pipeline:
/// 1. Age characters (natural death checks)
/// 2. Process queued player actions + NPC AI
/// 3. Resolve battles
/// 4. Move armies
/// 5. Economy (taxes, improvements, population)
/// 6. Unrest calculation
/// 7. Cohesion decay
/// 8. Random events (plague, famine, harvest, rebellion)
/// 9. Succession check
/// 10. Build TurnSummary
pub fn tick(world: &mut GameWorld, block_hash: &[u8; 32]) -> TurnSummary {
    tick::tick(world, *block_hash)
}

