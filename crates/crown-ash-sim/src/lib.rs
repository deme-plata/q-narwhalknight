/// Process a single player action against the current world state.
/// Returns a list of events generated by the action (may be empty).
pub fn process_action(world: &mut GameWorld, action: &QueuedAction) -> Vec<GameEvent> {
    let mut events = Vec::new();
    let turn = world.meta.turn;

    // Find the faction this wallet controls.
    let faction_id = match world.realm_by_wallet(&action.wallet) {
        Some(r) => r.faction,
        None => return events, // wallet not registered
    };

    match &action.action {
        GameAction::RaiseArmy { province } => {
            if let Some(prov) = world.province_mut_dirty(*province) {
                if prov.controller == faction_id && prov.garrison.levy >= 100 {
                    let levy_count = prov.garrison.levy / 2;
                    prov.garrison.levy -= levy_count;
                    let army_id = world.alloc_army_id();
                    let army = Army {
                        id: army_id,
                        owner_faction: faction_id,
                        commander: None,
                        troops: Troops {
                            levy: levy_count,
                            men_at_arms: 0,
                            knights: 0,
                        },
                        morale: FixedPoint::from_int(800),
                        location: *province,
                        destination: None,
                        movement_queue: Vec::new(),
                        raised_turn: turn,
                        supply: FixedPoint::from_int(100),
                        siege: None,
                    };
                    world.dirty.dirty_armies.insert(army_id);
                    world.dirty.armies_added.push(army_id);
                    world.armies.push(army);
                }
            }
        }
        GameAction::MoveArmy { army, target } => {
            let adjacent = world
                .army(*army)
                .and_then(|a| world.province(a.location))
                .map(|p| p.neighbors.contains(target))
                .unwrap_or(false);
            let owns = world.army(*army).map(|a| a.owner_faction == faction_id).unwrap_or(false);
            if adjacent && owns {
                if let Some(a) = world.army_mut_dirty(*army) {
                    a.destination = Some(*target);
                    a.movement_queue.clear(); // cancel any multi-hop
                }
            }
        }
        GameAction::MoveArmyPath { army, target } => {
            let owns = world.army(*army).map(|a| a.owner_faction == faction_id).unwrap_or(false);
            let location = world.army(*army).map(|a| a.location);
            if owns {
                if let Some(loc) = location {
                    if let Some(path) = map::find_path(loc, *target) {
                        if !path.is_empty() {
                            if let Some(a) = world.army_mut_dirty(*army) {
                                a.movement_queue = path;
                                a.destination = None;
                            }
                        }
                    }
                }
            }
        }
        GameAction::DisbandArmy { army } => {
            if let Some(pos) = world
                .armies
                .iter()
                .position(|a| a.id == *army && a.owner_faction == faction_id)
            {
                let disbanded = world.armies.remove(pos);
                world.dirty.armies_removed.push(disbanded.id);
                if let Some(prov) = world.province_mut_dirty(disbanded.location) {
                    prov.garrison.levy += disbanded.troops.levy;
                    prov.garrison.men_at_arms += disbanded.troops.men_at_arms;
                    prov.garrison.knights += disbanded.troops.knights;
                }
            }
        }
        GameAction::DeclareWar {
            target,
            casus_belli,
        } => {
            let target_id = *target;
            if target_id != faction_id {
                let realm_count = world.realms.len();
                for ridx in 0..realm_count {
                    if world.realms[ridx].faction == faction_id && !world.realms[ridx].at_war_with.contains(&target_id) {
                        world.realms[ridx].at_war_with.push(target_id);
                        world.dirty.dirty_realms.insert(faction_id);
                    }
                    if world.realms[ridx].faction == target_id && !world.realms[ridx].at_war_with.contains(&faction_id) {
                        world.realms[ridx].at_war_with.push(faction_id);
                        world.dirty.dirty_realms.insert(target_id);
                    }
                }
                if let Some(rel) = world.relation_mut_dirty(faction_id, target_id) {
                    rel.at_war = true;
                    rel.opinion -= FixedPoint::from_int(100);
                    rel.grievances.push(Grievance {
                        reason: format!("Declared war ({:?})", casus_belli),
                        opinion_modifier: FixedPoint::from_int(-100),
                        inflicted_turn: turn,
                        decay_turns_remaining: 200,
                    });
                }
                events.push(GameEvent::WarDeclared {
                    attacker: faction_id,
                    defender: target_id,
                    casus_belli: format!("{:?}", casus_belli),
                    turn,
                });
            }
        }
        GameAction::ProposeTreaty { target, treaty } => {
            let target_id = *target;
            let _ = apply_treaty(world, faction_id, target_id, *treaty);
            events.push(GameEvent::TreatySigned {
                faction_a: faction_id,
                faction_b: target_id,
                treaty_type: format!("{:?}", treaty),
                turn,
            });
        }
        GameAction::AcceptTreaty { from, treaty } => {
            let _ = apply_treaty(world, *from, faction_id, *treaty);
            events.push(GameEvent::TreatySigned {
                faction_a: *from,
                faction_b: faction_id,
                treaty_type: format!("{:?}", treaty),
                turn,
            });
        }
        GameAction::BuildImprovement {
            province,
            improvement,
        } => {
            let cost = improvement.build_cost_gold();
            let can_afford = world
                .realm_for_faction(faction_id)
                .map(|r| r.treasury >= cost)
                .unwrap_or(false);
            let owns = world
                .province(*province)
                .map(|p| p.controller == faction_id)
                .unwrap_or(false);
            let already_built = world
                .province(*province)
                .map(|p| p.improvements.contains(improvement))
                .unwrap_or(false);
            let in_queue = world
                .province(*province)
                .map(|p| p.construction_queue.iter().any(|&(imp, _)| imp == *improvement))
                .unwrap_or(false);
            if owns && can_afford && !already_built && !in_queue {
                if let Some(realm) = world.realm_for_faction_mut_dirty(faction_id) {
                    realm.treasury = realm.treasury - cost;
                }
                if let Some(prov) = world.province_mut_dirty(*province) {
                    prov.construction_queue
                        .push((*improvement, improvement.build_turns()));
                }
            }
        }
        GameAction::SetTaxRate { province, rate } => {
            if let Some(prov) = world.province_mut_dirty(*province) {
                if prov.controller == faction_id {
                    prov.tax_rate = (*rate).clamp(FixedPoint::ZERO, FixedPoint::ONE);
                }
            }
        }
        GameAction::AssignCouncilor { character, role } => {
            let char_faction = world.character(*character).map(|c| c.faction);
            if char_faction == Some(faction_id) {
                let target_role = match role {
                    CouncilRole::Marshal => CharacterRole::Marshal,
                    CouncilRole::Chaplain => CharacterRole::Chaplain,
                    CouncilRole::Steward => CharacterRole::Steward,
                    CouncilRole::Spymaster => CharacterRole::Spymaster,
                };
                let char_count = world.characters.len();
                for cidx in 0..char_count {
                    if world.characters[cidx].faction == faction_id
                        && world.characters[cidx].role == target_role
                        && world.characters[cidx].id != *character
                    {
                        world.characters[cidx].role = CharacterRole::Courtier;
                        world.dirty.dirty_characters.insert(world.characters[cidx].id);
                    }
                }
                if let Some(c) = world.character_mut_dirty(*character) {
                    c.role = target_role;
                }
            }
        }
        GameAction::DesignateHeir { character } => {
            let char_faction = world.character(*character).map(|c| c.faction);
            if char_faction == Some(faction_id) {
                let ruler_id = world.realm_for_faction(faction_id).map(|r| r.ruler);
                if let Some(rid) = ruler_id {
                    if let Some(ruler) = world.character_mut_dirty(rid) {
                        ruler.heir = Some(*character);
                    }
                    if let Some(heir) = world.character_mut_dirty(*character) {
                        heir.role = CharacterRole::Heir;
                    }
                }
            }
        }
        GameAction::ArrangeMarriage { a, b } => {
            let a_ok = world.character(*a).map(|c| c.alive && c.is_adult() && c.spouse.is_none()).unwrap_or(false);
            let b_ok = world.character(*b).map(|c| c.alive && c.is_adult() && c.spouse.is_none()).unwrap_or(false);
            let a_faction = world.character(*a).map(|c| c.faction);
            let b_faction = world.character(*b).map(|c| c.faction);
            let one_ours = a_faction == Some(faction_id) || b_faction == Some(faction_id);
            if a_ok && b_ok && one_ours {
                if let Some(ca) = world.character_mut_dirty(*a) {
                    ca.spouse = Some(*b);
                }
                if let Some(cb) = world.character_mut_dirty(*b) {
                    cb.spouse = Some(*a);
                }
                // Diplomatic opinion boost for cross-faction marriage.
                if a_faction != b_faction {
                    if let (Some(fa), Some(fb)) = (a_faction, b_faction) {
                        if let Some(rel) = world.relation_mut_dirty(fa, fb) {
                            rel.opinion += FixedPoint::from_int(30);
                        }
                    }
                }
                // Set up personal relationship + alliance event.
                let mut _marriage_events = Vec::new();
                relationships::on_marriage(world, *a, *b, &mut _marriage_events, world.meta.turn);
            }
        }
        GameAction::ConvertProvince { province, religion } => {
            let owns = world.province(*province)
                .map(|p| p.controller == faction_id)
                .unwrap_or(false);
            let different = world.province(*province)
                .map(|p| p.religion != *religion)
                .unwrap_or(false);
            let not_already_converting = world.province(*province)
                .map(|p| p.conversion_progress.is_none())
                .unwrap_or(false);
            if owns && different && not_already_converting {
                // Start gradual conversion (progress 0 → 1000 over multiple turns).
                if let Some(prov) = world.province_mut_dirty(*province) {
                    prov.conversion_progress = Some((*religion, FixedPoint::ZERO));
                    // Small initial unrest from announcing conversion.
                    prov.unrest += FixedPoint::from_int(30);
                }
            }
        }
        GameAction::LaunchPlot { target, plot_type } => {
            // Find a character in the player's faction to be the instigator (the ruler).
            let ruler_id = world.realm_for_faction(faction_id).map(|r| r.ruler);
            if let Some(instigator_id) = ruler_id {
                if let Ok(plot_id) = intrigue::launch_plot(world, instigator_id, *target, *plot_type) {
                    events.push(GameEvent::PlotLaunched {
                        instigator: instigator_id,
                        target: *target,
                        plot_type: plot_type.label().to_string(),
                        turn,
                    });
                    let _ = plot_id;
                }
            }
        }
        GameAction::BackPlot { plot_id } => {
            // Find a courtier or councillor to back the plot.
            let backer = world
                .characters
                .iter()
                .find(|c| {
                    c.faction == faction_id
                        && c.alive
                        && c.role != CharacterRole::Ruler
                        && c.role != CharacterRole::Heir
                })
                .map(|c| c.id);
            if let Some(backer_id) = backer {
                let _ = intrigue::back_plot(world, *plot_id, backer_id);
            }
        }
        GameAction::InvestigatePlot { spymaster } => {
            // Verify the spymaster belongs to the player's faction.
            let valid = world
                .character(*spymaster)
                .map(|c| c.faction == faction_id && c.role == CharacterRole::Spymaster && c.alive)
                .unwrap_or(false);
            if valid {
                let mut rng = random::DeterministicRng::new([turn as u8; 32], "investigate");
                let investigate_events = intrigue::investigate_plot(world, *spymaster, &mut rng);
                events.extend(investigate_events);
            }
        }
        GameAction::EstablishTradeRoute { from, to } => {
            // Validate: player controls 'from' province.
            let owns_from = world.province(*from)
                .map(|p| p.controller == faction_id)
                .unwrap_or(false);
            // Validate: provinces are adjacent.
            let adjacent = world.province(*from)
                .map(|p| p.neighbors.contains(to))
                .unwrap_or(false);
            // Validate: no existing route between these two.
            let already_exists = world.trade_routes.iter().any(|r| {
                (r.province_a == *from && r.province_b == *to)
                    || (r.province_a == *to && r.province_b == *from)
            });
            // Validate: per-province cap not exceeded for either endpoint.
            let routes_from = trade::routes_for_province(world, *from);
            let routes_to = trade::routes_for_province(world, *to);
            // Validate: global cap not exceeded.
            let under_global_cap = world.trade_routes.len() < trade::MAX_TRADE_ROUTES;

            if owns_from
                && adjacent
                && !already_exists
                && routes_from < trade::MAX_ROUTES_PER_PROVINCE
                && routes_to < trade::MAX_ROUTES_PER_PROVINCE
                && under_global_cap
            {
                let route_id = world.alloc_trade_route_id();
                let goods = trade_good_for_province(world, *from);
                world.trade_routes.push(TradeRoute {
                    id: route_id,
                    province_a: *from,
                    province_b: *to,
                    goods,
                    volume: FixedPoint::from_int(trade::INITIAL_VOLUME),
                    established_turn: turn,
                    disrupted: false,
                });
                events.push(GameEvent::TradeRouteEstablished {
                    from: *from,
                    to: *to,
                    goods: goods.label().to_string(),
                    turn,
                });
            }
        }
        GameAction::DisruptTradeRoute { route_id } => {
            // Extract route endpoint data first (immutable borrow).
            let route_data = world.trade_routes.iter()
                .find(|r| r.id == *route_id)
                .map(|r| (r.province_a, r.province_b));

            if let Some((prov_a, prov_b)) = route_data {
                // Check that the disrupting player is at war with at least
                // one endpoint's controller.
                let ctrl_a = world.province(prov_a).map(|p| p.controller);
                let ctrl_b = world.province(prov_b).map(|p| p.controller);
                let at_war_with_a = ctrl_a.map_or(false, |c| world.at_war(faction_id, c));
                let at_war_with_b = ctrl_b.map_or(false, |c| world.at_war(faction_id, c));

                if at_war_with_a || at_war_with_b {
                    // Now take the mutable borrow to update the route.
                    if let Some(route) = world.trade_routes.iter_mut().find(|r| r.id == *route_id) {
                        route.disrupted = true;
                    }
                    events.push(GameEvent::TradeRouteDisrupted {
                        from: prov_a,
                        to: prov_b,
                        reason: "Raided by enemy faction".to_string(),
                        turn,
                    });
                }
            }
        }
    }

    events
}

/// Determine the primary trade good exported by a province based on its improvements.
fn trade_good_for_province(world: &GameWorld, province_id: u16) -> TradeGood {
    let improvements = match world.province(province_id) {
        Some(p) => &p.improvements,
        None => return TradeGood::Luxuries,
    };

    if improvements.contains(&Improvement::Farmstead) {
        TradeGood::Grain
    } else if improvements.contains(&Improvement::Mine) {
        TradeGood::Iron
    } else if improvements.contains(&Improvement::Lumbercamp) {
        TradeGood::Timber
    } else if improvements.contains(&Improvement::Quarry) {
        TradeGood::Stone
    } else if improvements.contains(&Improvement::Stables) {
        TradeGood::Horses
    } else if improvements.contains(&Improvement::Temple) {
        TradeGood::Faith
    } else {
        TradeGood::Luxuries
    }
}

