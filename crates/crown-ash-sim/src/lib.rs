//! Crown & Ash — Core deterministic simulation engine.
//!
//! This crate implements the complete game logic for the Crown & Ash medieval
//! grand strategy game running on the Q-NarwhalKnight blockchain.
//!
//! # Public API
//!
//! - [`init_world`] — Create a new game world from a config and seed.
//! - [`tick`] — Advance the simulation by one turn (full 10-step pipeline).
//! - [`process_action`] — Submit a player action to the game world.
//! - [`query_world`] — Read-only query against the current world state.
//! - [`snapshot_world`] — Full world snapshot for client consumption.
//! - [`queue_action`] — Queue an action for processing on the next tick.
//!
//! # Determinism
//!
//! Every computation in this crate is deterministic. All RNG is derived from
//! block hashes via [`DeterministicRng`]. **No floating point** is used anywhere;
//! all numeric game state uses [`FixedPoint`](crown_ash_types::FixedPoint) (i64 x 1000).
//!
//! # Design Rules
//! - **No floating point** — all arithmetic uses `FixedPoint` (i64 * 1000).
//! - **No randomness from OS** — RNG is seeded exclusively from the block hash.
//! - **No allocation-heavy paths** — the entire tick must complete within the WASM
//!   gas budget (~10M gas).
//!
//! # Modules
//!
//! | Module       | Purpose                                        |
//! |--------------|------------------------------------------------|
//! | `world_state`| `GameWorld` struct holding all mutable state    |
//! | `world_gen`  | Initial world generation from seed              |
//! | `tick`       | 10-step turn pipeline                           |
//! | `combat`     | Auto-resolved battle math                       |
//! | `economy`    | Tax collection and prosperity                   |
//! | `cohesion`   | Realm stability decay and modifiers             |
//! | `succession` | Ruler death and heir resolution                 |
//! | `events`     | Random events (plague, famine, harvest, etc.)   |
//! | `ai`         | Heuristic NPC decision-making                   |
//! | `map`        | Fixed 25-province adjacency graph               |
//! | `random`     | Deterministic RNG from block hashes             |
//! | `trade`      | Inter-province trade routes and income           |
//! | `religion`   | Religious authority, gradual conversion, events  |
//! | `diplomacy`  | Vassal tribute, treaty expiry, coalitions        |
//! | `education`  | Age-gated skill growth, mentors, graduation      |

