/// Supported read-only queries against the game world.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub enum WorldQuery {
    /// Full metadata snapshot (turn, player count, etc.).
    Meta,
    /// Single province by ID.
    Province(ProvinceId),
    /// All provinces.
    AllProvinces,
    /// Single faction by ID.
    Faction(u8),
    /// All factions.
    AllFactions,
    /// Single character by ID.
    Character(u32),
    /// Characters belonging to a faction.
    FactionCharacters(u8),
    /// All armies.
    AllArmies,
    /// Armies belonging to a faction.
    FactionArmies(u8),
    /// Diplomatic relation between two factions.
    Diplomacy(u8, u8),
    /// A realm by faction ID.
    Realm(u8),
}

/// Serialisable snapshot of the game world for client consumption.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct WorldSnapshot {
    pub turn: u32,
    pub player_count: u8,
    pub provinces: Vec<Province>,
    pub characters: Vec<Character>,
    pub factions: Vec<Faction>,
    pub realms: Vec<Realm>,
    pub armies: Vec<Army>,
    pub diplomacy: Vec<DiplomaticRelation>,
}

/// Execute a read-only query and return JSON bytes.
pub fn query_world(world: &GameWorld, query: &WorldQuery) -> Vec<u8> {
    match query {
        WorldQuery::Meta => serde_json::to_vec(&world.meta).unwrap_or_default(),
        WorldQuery::Province(id) => {
            let p = world.province(*id);
            serde_json::to_vec(&p).unwrap_or_default()
        }
        WorldQuery::AllProvinces => serde_json::to_vec(&world.provinces).unwrap_or_default(),
        WorldQuery::Faction(id) => {
            let f = world.faction(*id);
            serde_json::to_vec(&f).unwrap_or_default()
        }
        WorldQuery::AllFactions => serde_json::to_vec(&world.factions).unwrap_or_default(),
        WorldQuery::Character(id) => {
            let c = world.character(*id);
            serde_json::to_vec(&c).unwrap_or_default()
        }
        WorldQuery::FactionCharacters(fid) => {
            let chars: Vec<_> = world
                .characters
                .iter()
                .filter(|c| c.faction == *fid)
                .collect();
            serde_json::to_vec(&chars).unwrap_or_default()
        }
        WorldQuery::AllArmies => serde_json::to_vec(&world.armies).unwrap_or_default(),
        WorldQuery::FactionArmies(fid) => {
            let armies: Vec<_> = world
                .armies
                .iter()
                .filter(|a| a.owner_faction == *fid)
                .collect();
            serde_json::to_vec(&armies).unwrap_or_default()
        }
        WorldQuery::Diplomacy(a, b) => {
            let rel = world.relation(*a, *b);
            serde_json::to_vec(&rel).unwrap_or_default()
        }
        WorldQuery::Realm(fid) => {
            let r = world.realm_for_faction(*fid);
            serde_json::to_vec(&r).unwrap_or_default()
        }
    }
}

/// Return a full snapshot of the world (all live characters, all entities).
pub fn snapshot_world(world: &GameWorld) -> WorldSnapshot {
    WorldSnapshot {
        turn: world.meta.turn,
        player_count: world.meta.player_count,
        provinces: world.provinces.clone(),
        characters: world.characters.iter().filter(|c| c.alive).cloned().collect(),
        factions: world.factions.clone(),
        realms: world.realms.clone(),
        armies: world.armies.clone(),
        diplomacy: world.diplomacy.clone(),
    }
}

/// Compute which provinces a faction can see (owns + adjacent to owned).
pub fn visible_provinces(world: &GameWorld, faction_id: u8) -> Vec<u16> {
    let mut visible = std::collections::HashSet::new();

    for p in &world.provinces {
        if p.controller == faction_id {
            visible.insert(p.id);
            // All neighbors are also visible.
            for &n in &p.neighbors {
                visible.insert(n);
            }
        }
    }

    let mut result: Vec<u16> = visible.into_iter().collect();
    result.sort_unstable();
    result
}

/// Return a fog-of-war snapshot filtered for a specific faction.
///
/// - Provinces outside visibility have population, garrison, resources, and
///   construction_queue hidden (zeroed out).
/// - Armies in non-visible provinces are excluded.
/// - Characters in non-visible factions with no visible armies are excluded.
/// - All factions and diplomacy are always visible (public knowledge).
pub fn snapshot_world_for_faction(world: &GameWorld, faction_id: u8) -> WorldSnapshot {
    let visible = visible_provinces(world, faction_id);

    // Filter provinces: show full data for visible, redacted for hidden.
    let provinces: Vec<Province> = world.provinces.iter().map(|p| {
        if visible.contains(&p.id) {
            p.clone()
        } else {
            // Redacted province: basic info only.
            Province {
                id: p.id,
                name: p.name.clone(),
                terrain: p.terrain,
                controller: p.controller,
                population: 0,           // hidden
                prosperity: FixedPoint::ZERO,
                unrest: FixedPoint::ZERO,
                fortification: 0,
                religion: p.religion,    // public knowledge
                culture: p.culture,      // public knowledge
                resources: crown_ash_types::province::Resources::default(),
                garrison: Troops::default(),
                improvements: vec![],
                construction_queue: vec![],
                scars: vec![],
                grudges: vec![],
                last_famine_turn: None,
                last_siege_turn: None,
                tax_rate: FixedPoint::ZERO,
                neighbors: p.neighbors.clone(),
                conversion_progress: None,
            }
        }
    }).collect();

    // Filter armies: only show those in visible provinces.
    let armies: Vec<Army> = world.armies.iter()
        .filter(|a| visible.contains(&a.location))
        .cloned()
        .collect();

    // Characters: show all in player's faction + characters with visible armies.
    let visible_army_commanders: std::collections::HashSet<u32> = armies.iter()
        .filter_map(|a| a.commander)
        .collect();

    let characters: Vec<Character> = world.characters.iter()
        .filter(|c| {
            c.alive && (
                c.faction == faction_id
                || visible_army_commanders.contains(&c.id)
                || c.role == CharacterRole::Ruler // rulers are public
            )
        })
        .cloned()
        .collect();

    WorldSnapshot {
        turn: world.meta.turn,
        player_count: world.meta.player_count,
        provinces,
        characters,
        factions: world.factions.clone(),
        realms: world.realms.clone(),
        armies,
        diplomacy: world.diplomacy.clone(),
    }
}

