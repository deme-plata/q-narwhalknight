fn apply_treaty(world: &mut GameWorld, faction_a: u8, faction_b: u8, treaty: TreatyType) -> Result<(), String> {
    let turn = world.meta.turn;
    match treaty {
        TreatyType::WhitePeace | TreatyType::Surrender => {
            if let Some(rel) = world.relation_mut_dirty(faction_a, faction_b) {
                rel.at_war = false;
                rel.treaties.push(ActiveTreaty {
                    treaty_type: format!("{:?}", treaty),
                    signed_turn: turn,
                    expires_turn: None,
                });
            }
            if let Some(realm) = world.realm_for_faction_mut_dirty(faction_a) {
                realm.at_war_with.retain(|&f| f != faction_b);
            }
            if let Some(realm) = world.realm_for_faction_mut_dirty(faction_b) {
                realm.at_war_with.retain(|&f| f != faction_a);
            }
        }
        TreatyType::NonAggression => {
            if let Some(rel) = world.relation_mut_dirty(faction_a, faction_b) {
                rel.treaties.push(ActiveTreaty {
                    treaty_type: "NonAggression".into(),
                    signed_turn: turn,
                    expires_turn: Some(turn + 100),
                });
                rel.opinion += FixedPoint::from_int(30);
            }
        }
        TreatyType::DefensiveAlliance => {
            if let Some(rel) = world.relation_mut_dirty(faction_a, faction_b) {
                rel.treaties.push(ActiveTreaty {
                    treaty_type: "DefensiveAlliance".into(),
                    signed_turn: turn,
                    expires_turn: None,
                });
                rel.opinion += FixedPoint::from_int(50);
            }
            if let Some(realm) = world.realm_for_faction_mut_dirty(faction_a) {
                if !realm.allies.contains(&faction_b) {
                    realm.allies.push(faction_b);
                }
            }
            if let Some(realm) = world.realm_for_faction_mut_dirty(faction_b) {
                if !realm.allies.contains(&faction_a) {
                    realm.allies.push(faction_a);
                }
            }
        }
        TreatyType::TradeAgreement => {
            if let Some(rel) = world.relation_mut_dirty(faction_a, faction_b) {
                rel.treaties.push(ActiveTreaty {
                    treaty_type: "TradeAgreement".into(),
                    signed_turn: turn,
                    expires_turn: Some(turn + 50),
                });
                rel.opinion += FixedPoint::from_int(20);
            }
        }
        TreatyType::Marriage => {
            if let Some(rel) = world.relation_mut_dirty(faction_a, faction_b) {
                rel.treaties.push(ActiveTreaty {
                    treaty_type: "Marriage".into(),
                    signed_turn: turn,
                    expires_turn: None,
                });
                rel.opinion += FixedPoint::from_int(40);
            }
        }
        TreatyType::Vassalization => {
            if let Some(rel) = world.relation_mut_dirty(faction_a, faction_b) {
                rel.treaties.push(ActiveTreaty {
                    treaty_type: "Vassalization".into(),
                    signed_turn: turn,
                    expires_turn: None,
                });
            }
            if let Some(realm) = world.realm_for_faction_mut_dirty(faction_a) {
                if !realm.vassals.contains(&faction_b) {
                    realm.vassals.push(faction_b);
                }
            }
        }
    }
    Ok(())
}

