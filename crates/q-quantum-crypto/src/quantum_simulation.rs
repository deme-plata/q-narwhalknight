/// Complex number representation for quantum amplitudes
#[derive(Debug, Clone, Copy, PartialEq, Serialize, Deserialize)]
pub struct ComplexAmplitude {
    /// Real component
    pub real: f64,
    /// Imaginary component  
    pub imag: f64,
}

impl ComplexAmplitude {
    pub fn new(real: f64, imag: f64) -> Self {
        Self { real, imag }
    }

    pub fn magnitude_squared(&self) -> f64 {
        self.real * self.real + self.imag * self.imag
    }

    pub fn normalize(&mut self, norm: f64) {
        let current_norm = self.magnitude_squared().sqrt();
        if current_norm > 0.0 {
            let factor = norm / current_norm;
            self.real *= factor;
            self.imag *= factor;
        }
    }
}

/// Quantum entanglement pair simulation
